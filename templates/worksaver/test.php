        <h3>Features</h3>

        <ul>
            <li>Maneuverable - goes anywhere a tractor can; gets into tight places.</li>
            <li>Higher productivity via it's unique cutting wheel design.</li>
            <li>Cutting wheel design provides a smoother cutting action.</li>
            <li>Easy to operate - hydraulic controls allow easy operation from the tractor seat using existing tractor hydraulic controls.</li>
            <li>Durable carbide-tipped cutting teeth are economical and easy to replace.</li>
            <li>Harden bushings used in major pivot points for increased reliability.</li>
            <li>Grease zerks provided at major pivot points for extended service life.</li>
            <li>Standard needle valve for precise control of cutting feed rate for best cutting action.</li>
            <li>SureFoot stabilizer bar provides solid footing during operation.</li>
            <li>Rubber deflector helps control chips and debris during grinding.</li>
            <li>Parking stand for easy storage.</li>
            <li>Tractor models require two pair of remote hydraulic outlets</li>
            <li>Hydraulic model has quick attach universal skid steer hitch system.</li>
            <li>skid steer unit has hydraulic solenoid valve requiring only one pair of remote hydraulic outlets.</li>
        </ul>
