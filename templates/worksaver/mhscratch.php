        Designed for skid steers, the
        LPTG series tine grapple easily
        moves rocks, brush, wood,
        metal and other materials while
        leaving the dirt on the ground.
        The grapple is ideal for various applications
        including construction site demolition and cleanup,
        storm clean-up, and landscaping. Features
        include 5-½” tine spacing, 3,000 psi cylinders
        with cylinder guards to protect the cylinders and
        components. Hoses are equipped with “connect
        under pressure” flat-faced couplers.
