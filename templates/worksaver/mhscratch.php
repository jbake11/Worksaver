<h2>Our Post Hole Diggers Dig Deeper, Faster & Smarter</h2><br />
<div class="row">
    <div class="col-lg-12 col-md-12 col-sm-12 col-xs-12">
        Engineered to allow the operator to dig deeper, faster and smarter, Worksaver’s complete
        line of PTO powered diggers offer time saving convenience with outstanding reliability.
        Shear-bolt protected driveline is coupled to a rugged gearbox featuring tapered roller bearings
        and spring-loaded seals for extended service life. Large 2” output shaft has two-bolt auger
        connection for reliable power transfer. All job matched augers utilize fishtail center points
        with double flighting on the first turn for quick, positive digging action right from the start.
