        Revolutionizing the way rock
        grapples work, the SARG
        (Sweep Action Rock Grapple)
        from Worksaver makes life easier.
        With its patent pending design,
        the SARG allows the operator to
        own one tool to sort large or small
        debris, resulting in low cost of
        ownership. The front grapple
        operates in a sweeping action to
        pull rock or debris into the unit.
        Grapple design retains the rocks
        or debris, allowing the operator to
        rock the attachment to sift dirt out
        of the material. Rocks, brush, logs,
        stumps, debris or scrap, the SARG
        handles them all.
