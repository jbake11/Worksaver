        <h4>Chain or Sprocket Breakage</h4>

        <ul>
            <li>The chain is too tight. Adjust the chain to allow 3/16" deflection. When reversing the direction of rotation, allow the bale to almost come to a complete stop before reversing.
            <li>The RW SpinOff must be run with the chain guard in place to prevent foreign objects from getting caught in the chain and causing damage.
        </ul>

        <h4>Chain Coming Off</h4>

        <ul>
            <li>The small sprocket and large sprocket are not aligned properly. Align the small sprocket with the large sprocket on the spinner assembly using a straight edge.
            <li>The chain may be too loose. Adjust the chain to allow 3/16" deflection.

        </ul>
