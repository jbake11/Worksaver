        <h3>Skid Steer Grader Blade</h3>
        <p>The patented SSGB-8A Grader is designed to be used by asphalt, landscaping, and concrete flatwork contractors. The unit has a 8-foot, six-way hydraulically controlled moldboard operated by an in-cab remote. With an optional laser system, the grader will maintain grade to tight tolerances. To help prevent damage, the moldboard is shear pin protected. Unit comes complete with 1/2” “connect under pressure” flat-faced hydraulic couplers and a reversible beveled cutting edge for long life. Optional End Plate Kit (Pair, #811980) and Laser Pole Kit (Pair, #812110) available.</p>

